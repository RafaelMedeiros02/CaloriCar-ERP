    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldPesquisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo para cadastrar!");
        } else {
            mU.setDescricao(jTextFieldPesquisa.getText());
            udao.cadastrarCategoria(mU);
            JOptionPane.showMessageDialog(null, jTextFieldPesquisa.getText() + " Adicionado(a) com sucesso!");
            this.dispose();
            new CadastrarCategorias().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextFieldPesquisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione qual categoria excluir!");
        } else {
            mU.setDescricao(jTextFieldPesquisa.getText());
            udao.excluirCategoria(mU);
            JOptionPane.showMessageDialog(null, jTextFieldPesquisa.getText() + " Excluido(a) com sucesso!");
            this.dispose();
            new CadastrarCategorias().setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

