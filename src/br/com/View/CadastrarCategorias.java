    public void pesquisaCategoria() {
        DefaultTableModel tabelaCategoria = (DefaultTableModel) jTableCategoria.getModel();
        try {
            tabelaCategoria.setNumRows(0);
            for (ModelUtil mU : udao.pesquisarCategoria(jTextFieldPesquisa.getText())) {
                tabelaCategoria.addRow(new Object[]{
                    mU.getCod_categoria(),
                    mU.getDescricao()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

