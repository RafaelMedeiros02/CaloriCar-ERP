    public void pesquisaTaxas() {
        DefaultTableModel tabelaTaxas = (DefaultTableModel) jTableTaxas.getModel();
        try {
            tabelaTaxas.setNumRows(0);
            for (ModelUtil mU : udao.pesquisarPagamentosTaxas(jTextFieldPesquisa.getText())) {
                tabelaTaxas.addRow(new Object[]{
                    mU.getCod_pagamento(),
                    mU.getMetodo(),
                    mU.getTaxa()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

