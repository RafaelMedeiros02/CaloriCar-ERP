    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldPesquisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo para cadastrar!");
        } else {
            mU.setMetodo(jTextFieldPesquisa.getText());
            udao.cadastrarTaxaPagamento(mU);
            JOptionPane.showMessageDialog(null, jTextFieldPesquisa.getText() + " Adicionado(a) com sucesso!");
            this.dispose();
            new CadastrarPagTax().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextFieldPesquisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione qual pagamento excluir!");
        } else {
            mU.setMetodo(jTextFieldPesquisa.getText());
            udao.excluirTaxaPag(mU);
            JOptionPane.showMessageDialog(null, jTextFieldPesquisa.getText() + " Excluido(a) com sucesso!");
            this.dispose();
            new CadastrarPagTax().setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

