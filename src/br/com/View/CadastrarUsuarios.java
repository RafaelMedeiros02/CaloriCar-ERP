    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        if (jTextFieldNome.getText().isEmpty()
                || jTextFieldUsuario.getText().isEmpty()
                || jPasswordFieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "É necessário todos os dados para cadastrar um usuário!");
        } else {
            int escolha = JOptionPane.showConfirmDialog(null, "Deseja cadastrar: " + jTextFieldNome.getText() + "como usuário ?");
            if (escolha == JOptionPane.YES_OPTION) {
                mU.setNomeCompleto(jTextFieldNome.getText());
                mU.setNomeUsuario(jTextFieldUsuario.getText());
                mU.setPermissao(String.valueOf(jComboBoxPerm.getSelectedItem()));
                mU.setSenha(jPasswordFieldSenha.getText());
                try {
                    udao.cadastrarUsuario(mU);
                    JOptionPane.showMessageDialog(null, jTextFieldNome.getText() + "Cadastrado com sucesso!");
                    this.dispose();
                    new CadastrarUsuarios().setVisible(true);
                } catch (Exception e) {

                }

            }

        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        if (jTextFieldNome.getText().isEmpty()
                || jTextFieldUsuario.getText().isEmpty()
                || jPasswordFieldSenha.getText().isEmpty()
                || jTextFieldCodUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "É necessário todos os dados para cadastrar um usuário!");
        } else {
            int escolha = JOptionPane.showConfirmDialog(null, "Deseja alterar dados de: " + jTextFieldNome.getText() + "?");
            if (escolha == JOptionPane.YES_OPTION) {
                mU.setNomeCompleto(jTextFieldNome.getText());
                mU.setNomeUsuario(jTextFieldUsuario.getText());
                mU.setPermissao(String.valueOf(jComboBoxPerm.getSelectedItem()));
                mU.setSenha(jPasswordFieldSenha.getText());
                mU.setCod_usuario(Integer.parseInt(jTextFieldCodUsuario.getText()));
                try {
                    udao.alteraDados(mU);
                    JOptionPane.showMessageDialog(null, jTextFieldNome.getText() + " Alterado com sucesso!");
                    this.dispose();
                    new CadastrarUsuarios().setVisible(true);
                } catch (Exception e) {

                }

            }

        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTextFieldCodUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "É necessário o código do usuário para excluir!");
        } else {
            int escolha = JOptionPane.showConfirmDialog(null, "Deseja excluir: " + jTextFieldNome.getText());
            if (escolha == JOptionPane.YES_OPTION) {
                mU.setCod_usuario(Integer.parseInt(jTextFieldCodUsuario.getText()));
                try {
                    udao.excluirUsuario(mU);
                    JOptionPane.showMessageDialog(null, jTextFieldNome.getText() + "Excluido com sucesso!");
                    this.dispose();
                    new CadastrarUsuarios().setVisible(true);
                } catch (Exception e) {

                }

            }

        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

