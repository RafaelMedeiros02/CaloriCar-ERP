    public void pesquisa_parcelamentos() {
        DefaultTableModel tabelaParcelas = (DefaultTableModel) jTableParcelas.getModel();
        try {
            tabelaParcelas.setNumRows(0);
            for (ModelVenda mV : vdao.pesquisa_parcelas(jTextFieldPesquisaCliente.getText())) {
                tabelaParcelas.addRow(new Object[]{
                    mV.getRefVenda(),
                    mV.getClienteParcela(),
                    mV.getDataParcelaS(),
                    mV.getValorParcela(),
                    mV.getCpf(),
                    mV.getTotalCompra(),
                    mV.getStatus(),
                    mV.getDesc()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        jLabelValor.setText(new DecimalFormat("R$ #,##0.00").format(somaValorTotal()));

    }

    public void pesquisa_Par() {
        DefaultTableModel tabela = (DefaultTableModel) jTableParcelas.getModel();
        try {

            tabela.setNumRows(0);
            for (ModelVenda mV : vdao.parcelas_data((new SimpleDateFormat("dd/MM/yyyy").format(jDateInicio.getDate())), (new SimpleDateFormat("dd/MM/yyyy").format(jDateFim.getDate())))) {
                tabela.addRow(new Object[]{
                    mV.getRefVenda(),
                    mV.getClienteParcela(),
                    mV.getDataParcelaS(),
                    mV.getValorParcela(),
                    mV.getCpf(),
                    mV.getTotalCompra(),
                    mV.getStatus(),
                    mV.getDesc()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        jLabelValor.setText(new DecimalFormat("R$ #,##0.00").format(somaValorTotal()));

    }

    public void pesquisa_parcelamentos_status() {
        DefaultTableModel tabelaParcelas = (DefaultTableModel) jTableParcelas.getModel();
        try {
            tabelaParcelas.setNumRows(0);
            for (ModelVenda mV : vdao.pesquisa_parcelas_status(String.valueOf(jComboBoxStatus.getSelectedItem()))) {
                tabelaParcelas.addRow(new Object[]{
                    mV.getRefVenda(),
                    mV.getClienteParcela(),
                    mV.getDataParcelaS(),
                    mV.getValorParcela(),
                    mV.getCpf(),
                    mV.getTotalCompra(),
                    mV.getStatus(),
                    mV.getDesc()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        jLabelValor.setText(new DecimalFormat("R$ #,##0.00").format(somaValorTotal()));

    }

    public float somaValorTotal() {

        soma = 0;
        valor = 0;
        int cont = jTableParcelas.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(jTableParcelas.getValueAt(i, 3)));
            soma += valor;
        }
        return soma;
    }

