    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldCodBarras.getText().isEmpty()
                || jTextFieldQtde.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {

            int escolha = JOptionPane.showConfirmDialog(null, "Confirma a devolução?");
            if (escolha == JOptionPane.YES_OPTION) {
                mD.setCodBarras(jTextFieldCodBarras.getText());
                mD.setMotivo(jTextFieldMotivo.getText());
                mD.setDescricao(jTextFieldDescricao.getText());
                mD.setUsuario(ModelUsuario.nomeSession);
                mD.setCodCupom(Integer.parseInt(jTextFieldCodCupom.getText()));
                mD.setQtde(Integer.parseInt(jTextFieldQtde.getText()));
                mD.setValor(fv.converterVirgulaParaPonto(jTextFieldValor.getText()));
                mD.setDataVenda(new SimpleDateFormat("dd/MM/yyyy").format(DataVenda.getDate()));
                mD.setDataDevolucao(new SimpleDateFormat("dd/MM/yyyy").format(DataDevolucao.getDate()));
                ddao.gravaDevolucao(mD);
                mEE.setCod_barras(jTextFieldCodBarras.getText());
                mEE.setQtde_entrada(Integer.parseInt(jTextFieldQtde.getText()));
                edao.alteraEstoque(mEE);
                JOptionPane.showMessageDialog(null, "Devolução efetuada com sucesso!");
                this.dispose();
                new Devolucao().setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

