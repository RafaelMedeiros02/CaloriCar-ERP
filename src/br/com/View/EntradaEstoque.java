    private void jButtonConfirmaEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmaEntradaActionPerformed
        if (jTextFieldCodBarras.getText().isEmpty()
                || jTextFieldDescricao.getText().isEmpty()
                || jTextFieldFornecedor.getText().isEmpty()
                || jFormattedTextFieldDataEntrada.getText().isEmpty()
                || jTextFieldQtdeEntrada.getText().isEmpty()
                || jTextFieldValorEntrada.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para realizar a entrada!", "Alerta", JOptionPane.WARNING_MESSAGE);
        } else {
            int escolha = JOptionPane.showConfirmDialog(null, "Confirmar Entrada: " + jTextFieldDescricao.getText());
            if (escolha == JOptionPane.YES_OPTION) {
                mEE.setCod_barras(jTextFieldCodBarras.getText());
                mEE.setDescricao(jTextFieldDescricao.getText());
                mEE.setFornecedor(jTextFieldFornecedor.getText());
                mEE.setData_entrada(jFormattedTextFieldDataEntrada.getText());
                mEE.setQtde_entrada(Integer.parseInt(jTextFieldQtdeEntrada.getText()));
                mEE.setValor_entrada(fv.converterVirgulaParaPonto(jTextFieldValorEntrada.getText()));
                try {
                    edao.gravarEntrada(mEE);
                    edao.alteraEstoque(mEE);
                    JOptionPane.showMessageDialog(null, "Entrada realizada com sucesso, confira os novos valores de estoque!");
                    edao.atualizaStatus_Alerta();
                    edao.atualizaStatus_ESGOTADO();
                    edao.atualizaStatus_Estoque();

                    //caixa 
                    Timestamp timestamp = new Timestamp(System.currentTimeMillis());
                    String date = new SimpleDateFormat("dd/MM/yyyy - H:mm:ss").format(timestamp.getTime());
                    mMC.setDataHora(date);
                    mMC.setValor_saida(mEE.getValor_entrada());

                    escolha = JOptionPane.showConfirmDialog(null, "Deseja realizar a entrada de outros produtos ao seu estoque?");
                    if (escolha == JOptionPane.YES_OPTION) {
                        this.dispose();
                        new AlterarSelecionado().setVisible(true);
                    } else {
                        this.dispose();
                        new PesquisarEstoque().setVisible(true);
                    }

                } catch (Exception e) {

                }

            }
        }

    }//GEN-LAST:event_jButtonConfirmaEntradaActionPerformed

