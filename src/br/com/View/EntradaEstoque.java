    public void determinaSituacao() {
        int qtde = Integer.parseInt(jTextFieldQtdeAtual.getText());

        if (qtde == 0) {
            jLabelSituacao.setText("Esgotado");

            jLabelSituacao.setForeground(java.awt.Color.RED);

        } else {
            if (qtde > 0 && qtde <= 10) {
                jLabelSituacao.setText("Ultimas Unidades");

                jLabelSituacao.setForeground(java.awt.Color.ORANGE);

            }

            if (qtde > 10) {

                jLabelSituacao.setText("Em Estoque");

                jLabelSituacao.setForeground(java.awt.Color.BLUE);

            }

        }

    }

