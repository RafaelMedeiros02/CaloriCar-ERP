    public void pesquisaListaCompras() {
        DefaultTableModel tabelaCompras = (DefaultTableModel) jTableCompras.getModel();
        try {
            tabelaCompras.setNumRows(0);
            for (ModelCompras mC : cdao.pesquisaListaCompras(jTextFieldPesquisa.getText())) {
                tabelaCompras.addRow(new Object[]{
                    mC.getCod_compra(),
                    mC.getDescricao(),
                    mC.getUsuario(),
                    mC.getData_compra()

                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

