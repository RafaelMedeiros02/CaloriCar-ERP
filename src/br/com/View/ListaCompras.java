    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String descricaoCompra = JOptionPane.showInputDialog(null, "Descreva o que deseja adicionar a lista de compras!");
        if (descricaoCompra.isEmpty()) {
            JOptionPane.showMessageDialog(null, "É necessário informar algo para ser adicionado a lista de compras!");
        } else {
            mCO.setDescricao(descricaoCompra);
            mCO.setUsuario(ModelUsuario.nomeSession);
            Timestamp timestamp = new Timestamp(System.currentTimeMillis());
            String date = new SimpleDateFormat("dd/MM/yyyy").format(timestamp.getTime());
            mCO.setData_compra(date);
            cdao.cadastrarCompra(mCO);
            JOptionPane.showMessageDialog(null, "Adicionado(a) com sucesso a lista de compras!");
            this.dispose();
            new ListaCompras().setVisible(true);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

