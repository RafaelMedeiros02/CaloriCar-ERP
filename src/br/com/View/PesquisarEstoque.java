    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int escolha = JOptionPane.showConfirmDialog(null, "Realizar Entrada? ", "Informações", JOptionPane.YES_NO_OPTION);
        if (escolha == JOptionPane.YES_OPTION) {
            mE.setCod_barras(codigo);
            edao.pesquisaInfoEstoque(mE);
            eE.jTextFieldCodBarras.setText(mE.getCod_barras());
            eE.jTextFieldDescricao.setText(mE.getDescricao());
            eE.jTextFieldFornecedor.setText(mE.getFornecedor());
            eE.jTextFieldQtdeAtual.setText(String.valueOf(mE.getQuantidade()));
            mEE.setCod_barras(codigo);
            edao.pesquisaEntradas(mEE);
            eE.jTextFieldUltimaEntrada.setText(mEE.getUltima_entrada());
            this.dispose();
            eE.setVisible(true);
        } else {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

