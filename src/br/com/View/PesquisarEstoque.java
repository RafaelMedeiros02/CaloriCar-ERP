    public void pesquisaEstoque_Descricao() {
        DefaultTableModel tabelaEstoque = (DefaultTableModel) jTableEstoque.getModel();
        try {
            tabelaEstoque.setNumRows(0);
            for (ModelEstoque mE : edao.pesquisarEstoque(jTextFieldPesquisa.getText())) {
                tabelaEstoque.addRow(new Object[]{
                    mE.getCod_barras(),
                    mE.getDescricao(),
                    mE.getCategoria(),
                    mE.getFornecedor(),
                    mE.getPreco_custo(),
                    mE.getPreco_venda(),
                    mE.getLucro_unidade(),
                    mE.getQuantidade(),
                    mE.getAlerta_estoque(),
                    mE.getStatus_estoque()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

