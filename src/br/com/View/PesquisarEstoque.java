    private void jTableEstoqueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEstoqueMouseClicked
         if (evt.getButton() == MouseEvent.BUTTON3) {
          int setar = jTableEstoque.getSelectedRow();
          JOptionPane.showMessageDialog(null, jTableEstoque.getModel().getValueAt(setar, 1).toString());
         
         } else {
        int setar = jTableEstoque.getSelectedRow();
        codigo = (jTableEstoque.getModel().getValueAt(setar, 0).toString());

        int escolha = JOptionPane.showConfirmDialog(null, "Visualizar ficha completa? ", "Informações", JOptionPane.YES_NO_OPTION);
        if (escolha == JOptionPane.YES_OPTION) {

            mE.setCod_barras(codigo);
            edao.pesquisaInfoEstoque(mE);
            CadastroDeEstoque cE = new CadastroDeEstoque();
            cE.jTextFieldDescricaoProduto.setText(mE.getDescricao());
            cE.jTextFieldCodBarras.setText(mE.getCod_barras());
            cE.jTextFieldPrecoVenda.setText(String.valueOf(mE.getPreco_venda()));
            cE.jTextFieldPrecoCusto.setText(String.valueOf(mE.getPreco_custo()));
            cE.jTextFieldLucroUnidade.setText(String.valueOf(mE.getLucro_unidade()));
            cE.jComboBoxCategoria.setSelectedItem(String.valueOf(mE.getCategoria()));
            cE.jComboBoxFornecedor.setSelectedItem(String.valueOf(mE.getFornecedor()));
            cE.jTextFieldAlertaEstoque.setText(String.valueOf(mE.getAlerta_estoque()));
            cE.jTextFieldGetFornecedor.setText(mE.getFornecedor());
            cE.setVisible(true);
        } else {

        }
         }
    }//GEN-LAST:event_jTableEstoqueMouseClicked
    


    
    
              
	
