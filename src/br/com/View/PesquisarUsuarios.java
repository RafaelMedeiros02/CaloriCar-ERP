    public void pesquisaUsuarios() {
        DefaultTableModel tabelaUsuarios = (DefaultTableModel) jTableUsuarios.getModel();
        try {
            tabelaUsuarios.setNumRows(0);
            for (ModelUsuario mU : udao.pesquisarUsuarios(jTextFieldPesquisa.getText())) {
                tabelaUsuarios.addRow(new Object[]{
                    mU.getCod_usuario(),
                    mU.getNomeCompleto(),
                    mU.getNomeUsuario(),
                    mU.getPermissao()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

