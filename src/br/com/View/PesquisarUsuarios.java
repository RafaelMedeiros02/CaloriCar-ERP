    private void jTableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuariosMouseClicked
        int setar = jTableUsuarios.getSelectedRow();
        codigo = Integer.parseInt(jTableUsuarios.getModel().getValueAt(setar, 0).toString());
        int escolha = JOptionPane.showConfirmDialog(null, "Visualizar ficha completa? ", "Informações", JOptionPane.YES_NO_OPTION);
        if (escolha == JOptionPane.YES_OPTION) {
            mU.setCod_usuario(codigo);
            udao.pesquisaInfoUsuarios(mU);
            CadastrarUsuarios cU = new CadastrarUsuarios();
            cU.jTextFieldNome.setText(mU.getNomeCompleto());
            cU.jTextFieldUsuario.setText(mU.getNomeUsuario());
            cU.jTextFieldCodUsuario.setText(String.valueOf(mU.getCod_usuario()));
            cU.jComboBoxPerm.setSelectedItem(String.valueOf(mU.getPermissao()));
            cU.jPasswordFieldSenha.setText(mU.getSenha());
            cU.setVisible(true);
        } else {

        }
    }//GEN-LAST:event_jTableUsuariosMouseClicked

