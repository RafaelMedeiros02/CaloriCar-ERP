    public void lista() {
        DefaultTableModel tabela = (DefaultTableModel) jTableDevolucao.getModel();
        try {

            tabela.setNumRows(0);
            for (ModelDevolucao mD : ddao.listar((new SimpleDateFormat("dd/MM/yyyy").format(jDateInicio.getDate())), (new SimpleDateFormat("dd/MM/yyyy").format(jDateFim.getDate())))) {
                tabela.addRow(new Object[]{
                    mD.getDescricao(),
                    mD.getMotivo(),
                    mD.getDataDevolucao(),
                    mD.getCodCupom(),
                    mD.getQtde(),
                    mD.getValor()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

