    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            this.connection = new ConnectionFactory().getConnection();
            HashMap param = new HashMap();
            param.put("data_inicio", (new SimpleDateFormat("dd/MM/yyyy").format(jDateInicio.getDate())));
            param.put("data_fim", (new SimpleDateFormat("dd/MM/yyyy").format(jDateFim.getDate())));
            JasperPrint rel = null;
            rel = JasperFillManager.fillReport("Relatorios/relatorioDevolucao.jasper", param, connection);
            JasperViewer.viewReport(rel, false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

